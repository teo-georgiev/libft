#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (0);
	if (!content)
		node->content = 0;
	else
		node->content = content;
	node->next = 0;
	return (node);
}
